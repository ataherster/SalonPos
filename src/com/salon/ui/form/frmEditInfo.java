    void Init_Data () {
        try {
            stmGetData=Main.data.getActiveConnection().createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);

            init_PreparedStatement();

            view_Data();
        } catch (SQLException ex) {
            Logger.getLogger(frmEditInfo.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(rootPane, ex, "err : init Prepared Statement", JOptionPane.ERROR_MESSAGE);
        }
    }

    void view_Data () throws SQLException {
        rsGetData=stmGetData.executeQuery("SELECT ALAMAT, KOTA, TELPCAB FROM CABANG");
        if (rsGetData.next()) {
            txtAlamat.setText(rsGetData.getObject("ALAMAT").toString());
            txtKota.setText(rsGetData.getObject("KOTA").toString());
            txtTelp.setText(rsGetData.getObject("TELPCAB").toString());
        }

        rsGetData=stmGetData.executeQuery("SELECT PESAN FROM PESAN");
        if (rsGetData.next()) {
            txtPesan.setText(rsGetData.getObject("PESAN").toString());
        }
    }
    
    void init_PreparedStatement () throws SQLException {
        stmUpdateInfo=Main.data.getActiveConnection().prepareStatement("UPDATE CABANG SET ALAMAT=?, KOTA=?, TELPCAB=?");
        stmUpdatePesan=Main.data.getActiveConnection().prepareStatement("UPDATE PESAN SET PESAN=?");
    }

    void Update_Pesan () throws SQLException {
        stmUpdateInfo.setObject(1, txtAlamat.getText());
        stmUpdateInfo.setObject(2, txtKota.getText());
        stmUpdateInfo.setObject(3, txtTelp.getText());
        
        stmUpdatePesan.setObject(1, txtPesan.getText());

        stmUpdateInfo.execute();
        stmUpdatePesan.execute();
    }
    
