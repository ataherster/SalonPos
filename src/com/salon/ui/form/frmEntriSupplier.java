    void initData () {
        try {
            init_PreparedStatement();
        } catch (SQLException ex) {
            Logger.getLogger(frmEntriSupplier.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    void init_PreparedStatement () throws SQLException {
        stmInsertSupplier=Main.data.getActiveConnection().prepareStatement("SELECT INSERT_SUPPLIER (?,?,?,?,?,?,?,?,?,?,?,?)");
    }

    void InsertSupplier () throws SQLException {
        stmInsertSupplier.setObject(1, txtNamaPerusahaanSupplier.getText());
        stmInsertSupplier.setObject(2, txtContact.getText());
        stmInsertSupplier.setObject(3, txtAlamat.getText());
        stmInsertSupplier.setObject(4, txtKota.getText());
        stmInsertSupplier.setObject(5, txtWilayah.getText());
        stmInsertSupplier.setObject(6, txtKodePos.getText());
        stmInsertSupplier.setObject(7, txtNegara.getText());
        stmInsertSupplier.setObject(8, txtTelpSupplier.getText());
        stmInsertSupplier.setObject(9, txtFax.getText());
        stmInsertSupplier.setObject(10, txtHomePage.getText());
        stmInsertSupplier.setObject(11, txtBank.getText());
        stmInsertSupplier.setObject(12, txtNoRek.getText());

        stmInsertSupplier.execute();
    }
